    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        try {
            if(no.getText().equals("") || judul.getText().equals("") || pengarang.getText().equals("") || tahun.getText().equals("") || penerbit.getText().equals("") || eksemplar.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi data dengan lengkap");
            return;
            }
            
            Statement stat = con.createStatement();
            

            String query = "UPDATE library SET judulbuku=?,author=?,tahun=?,publisher=?,eksemplar=? WHERE nobuku=?";
            
            // Buat mysql insert preparedstatement
            PreparedStatement preparedStmt = con.prepareStatement(query);
            preparedStmt.setString   (1, judul.getText());
            preparedStmt.setString   (2, pengarang.getText());
            preparedStmt.setString   (3, tahun.getText());
            preparedStmt.setString   (4, penerbit.getText());
            preparedStmt.setString   (5, eksemplar.getText());
            preparedStmt.setString   (6, no.getText());
           
            // Eksekusi preparedstatement
            preparedStmt.execute();
            
            kosongkan();
            tabel();
            JOptionPane.showMessageDialog(rootPane, "Data berhasil Diupdate");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_editActionPerformed

    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        try {       
            if(no.getText().equals("") || judul.getText().equals("") || pengarang.getText().equals("") || tahun.getText().equals("") || penerbit.getText().equals("") || eksemplar.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi data dengan lengkap");
            return;
            }
            
            
            // Persiapkan string query
            String query = "INSERT INTO library " +" values (?,?,?,?,?,?)";
            PreparedStatement preparedStmt = con.prepareStatement(query);
            preparedStmt.setString (1, no.getText());
            preparedStmt.setString   (2, judul.getText());
            preparedStmt.setString   (3, pengarang.getText());
            preparedStmt.setString   (4, tahun.getText());
            preparedStmt.setString   (5, penerbit.getText());
            preparedStmt.setString   (6, eksemplar.getText());
            
            
            // Eksekusi preparedstatement
            preparedStmt.execute();
            
            
            kosongkan();//Mengosongkan text field setelah berhasil menyimpan data
            tabel();// Menampilkan isi tabel
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan...");
      
            // Tutup koneksi
//            con.close();          
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_simpanActionPerformed

