    private void tabel() throws SQLException {
        Statement stat = con.createStatement();
        ResultSet res = stat.executeQuery("SELECT * FROM library");
        
        DefaultTableModel tb = new DefaultTableModel();
        // Memberi nama pada setiap kolom tabel
        tb.addColumn("NoBuku");
        tb.addColumn("JudulBuku");
        tb.addColumn("Author");
        tb.addColumn("Tahun");
        tb.addColumn("Publisher");
        tb.addColumn("Eksemplar");
        tabel.setModel(tb);
        
        
     
        while(res.next()) {
                Object[] data = {
                    res.getString("NoBuku"),
                    res.getString("JudulBuku"),
                    res.getString("Author"),
                    res.getString("Tahun"),
                    res.getString("Publisher"),
                    res.getString("Eksemplar")
                };
                // Tambah baris dengan data dari database 
                tb.addRow(data);
                tabel.setModel(tb);  
            }
    }
    
